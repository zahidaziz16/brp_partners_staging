	public function getProducts($data = array()) {
		$sql = "SELECT * FROM " . DB_PREFIX . "product p LEFT JOIN " . DB_PREFIX . "product_description pd ON (p.product_id = pd.product_id) WHERE pd.language_id = '" . (int)$this->config->get('config_language_id') . "'";

		if (!empty($data['filter_name'])) {
			$sql .= " AND pd.name LIKE '%" . $this->db->escape($data['filter_name']) . "%'";
		}

		if (!empty($data['filter_model'])) {
			$sql .= " AND p.model LIKE '%" . $this->db->escape($data['filter_model']) . "%'";
		}

		if (!empty($data['filter_name_model'])) {
			$sql .= " AND (pd.name LIKE '%" . $this->db->escape($data['filter_name_model']) . "%' OR p.model LIKE '%" . $this->db->escape($data['filter_name_model']) . "%')";
		}

		if (isset($data['filter_price']) && !is_null($data['filter_price'])) {
			$sql .= " AND p.price LIKE '" . $this->db->escape($data['filter_price']) . "%'";
		}

		if (isset($data['filter_quantity']) && !is_null($data['filter_quantity'])) {
			$sql .= " AND p.quantity = '" . (int)$data['filter_quantity'] . "'";
		}

		if (isset($data['filter_status']) && !is_null($data['filter_status'])) {
			$sql .= " AND p.status = '" . (int)$data['filter_status'] . "'";
		}
                
//              echo $data['filter_product_type'];exit;
                
//                if(is_null($data['filter_product_type'])) {
//                    echo "huh";exit;
//                }
                
                if (isset($data['filter_product_type']) && !is_null($data['filter_product_type'])) {
                    if($data['filter_product_type'] == 0) {
                        $sql .= " AND p.data_source = ''";
                    }else {
                        $sql .= " AND p.data_source != ''";
                    }
			
		}

		if (isset($data['filter_image']) && !is_null($data['filter_image'])) {
			if ($data['filter_image'] == 1) {
				$sql .= " AND (p.image IS NOT NULL AND p.image <> '' AND p.image <> 'no_image.png')";
			} else {
				$sql .= " AND (p.image IS NULL OR p.image = '' OR p.image = 'no_image.png')";
			}
		}

		if (isset($data['filter_products']) && is_array($data['filter_products']) && count($data['filter_products'])>0) {
			$sql .= " AND p.product_id IN ('".implode("','",$data['filter_products'])."')";
		}

		$sql .= " GROUP BY p.product_id";
		//echo $sql;exit;

		$sort_data = array(
			'pd.name',
			'p.model',
			'p.price',
			'p.quantity',
			'p.status',
                        'p.data_source',
			'p.sort_order'
		);

		if (isset($data['sort']) && in_array($data['sort'], $sort_data)) {
			$sql .= " ORDER BY " . $data['sort'];
		} else {
			$sql .= " ORDER BY pd.name";
		}

		if (isset($data['order']) && ($data['order'] == 'DESC')) {
			$sql .= " DESC";
		} else {
			$sql .= " ASC";
		}

		if (isset($data['start']) || isset($data['limit'])) {
			if ($data['start'] < 0) {
				$data['start'] = 0;
			}

			if ($data['limit'] < 1) {
				$data['limit'] = 20;
			}

			$sql .= " LIMIT " . (int)$data['start'] . "," . (int)$data['limit'];
		}
//                echo $sql;exit;
		$query = $this->db->query($sql);

		return $query->rows;
	}

